#!/bin/bash

git add . #: 모든 변경된 파일을 스테이징 영역에 추가합니다[1][3].
git commit -m "~~"  #: 스테이징된 변경사항을 커밋합니다[1][3].

## 브랜치 관리
git branch #: 현재 브랜치 목록을 확인합니다[1]
git branch [브랜치명] # : 새 브랜치를 생성합니다[1].
git checkout [브랜치명] # : 특정 브랜치로 전환합니다[1].
git merge [브랜치명] # : 현재 브랜치에 다른 브랜치의 변경사항을 병합합니다[1].

## 원격 저장소 연동
git remote add origin [URL]     # : 원격 저장소를 추가합니다[1].
git push origin work      # : 로컬 변경사항을 원격 저장소에 업로드합니다[1].
git pull                        # : 원격 저장소의 변경사항을 로컬로 가져옵니다[1].


## 저장소 관리
git init # : 현재 디렉토리를 Git 저장소로 초기화합니다[1][3].
git clone [URL] # : 원격 저장소를 로컬에 복제합니다[1].